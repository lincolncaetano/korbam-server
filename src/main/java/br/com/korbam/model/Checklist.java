package br.com.korbam.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

@Entity
@Table(name = "checklist")
public class Checklist implements Serializable{

	private static final long serialVersionUID = 3094938342174310623L;
	
	@Id
	@GeneratedValue
	@Column(name="ID_CHECKLIST")
	private Long id;
	
	@ManyToOne
    @JoinColumn(name = "ID_EVENTO")  
	private Evento evento;
	
	@Column(name="DESCRICAO", length=45)
	private String descricao;
	
	@Column(name="STATUS", length=1)
	private String status;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Evento getEvento() {
		return evento;
	}

	public void setEvento(Evento evento) {
		this.evento = evento;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Checklist other = (Checklist) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Checklist [id=" + id + ", evento=" + evento + ", descricao=" + descricao + ", status=" + status + "]";
	}
	
	

}
